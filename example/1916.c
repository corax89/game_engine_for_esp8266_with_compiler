void step(){
	int r;
	if(gettimer(0) == 0){
		settimer(0, 3000);
		if(random(4) == 0)
			addUnit(aircraft3, 18, 12, random(3) + 1, 8);
		else
			addUnit(aircraft2, 12, 12, random(6) + 1, 2); 
	}
	if(gettimer(2) == 0){
		settimer(2, 200);
		if(godmode > 0){
			godmode--;
			if(godmode % 2)
				getsprite(16,aircraft1a);
			else
				getsprite(16,aircraft1);
			
		}
		printtosprite(3, 1, score);
		r = random(unitcount);
		if(spritegetvalue(r + 17,S_LIVES) > 0){
			putsprite(nabullet + 9, spritegetvalue(r + 17,S_X) + 4, spritegetvalue(r + 17,S_Y) + 14);
			nabullet++;
			if(nabullet > 7)
				nabullet = 0;
		}
	}
	if(gettimer(3) == 0){
		r = random(2);
		if(r == 1){
			for(i = 0; i < unitcount / 2; i++)
				spritesetvalue(i + 17,S_SPEEDX,-4);
		}
		else if(r == 2){
			for(i = unitcount / 2;i < unitcount; i++)
				spritesetvalue(i + 17,S_SPEEDX,4);
		}
		settimer(3, 10000);
	}
	key = getkey();
	setcolor(1);
	if((key & KEY_LEFT) && (x > 1))
		x-=2;
	if((key & KEY_RIGHT) && (x < 117))
		x+=2;
	if((key & KEY_UP) && (y > 1))
		y-=2;
	if((key & KEY_DOWN) && (y < 116))
		y+=2;
	if(gettimer(1) == 0){
		settimer(1, 200);
		putsprite(nbullet, x + 4, y + 1);
		nbullet++;
		if(nbullet > 8)
			nbullet = 1;
	}
	putsprite(16, x, y);
	scroll(3);
	ypos++;
	if(ypos >= 0){
		drawtile(1,TILE_OFFSET);
		ypos = TILE_OFFSET;
	}
}

void main(){
	while(1){
		init();
		while(game){
			step();
			delayredraw();
		}
		end();
	}
}
